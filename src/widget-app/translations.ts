export type WidgetLanguage = keyof typeof widgetTranslations;
export type WidgetTranslationKey = keyof typeof widgetTranslations.en;

export function detectWidgetLanguage(preferredLanguage?: string): WidgetLanguage {
	const raw = String(preferredLanguage || "").trim().toLowerCase().replace("_", "-");
	const base = raw.split("-")[0];
	if (base === "pt" || base === "es" || base === "en") {
		return base;
	}
	const browserLanguage = navigator.language.toLowerCase().split("-")[0];
	if (browserLanguage === "pt" || browserLanguage === "es") {
		return browserLanguage;
	}
	return "en";
}
